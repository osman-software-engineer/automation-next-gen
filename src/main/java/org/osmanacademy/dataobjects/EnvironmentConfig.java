package org.osmanacademy.dataobjects;
public class EnvironmentConfig {
    private String envName;
    private String websiteUrl;
    private String dbHostName;
    private String dbName;
    private String dbPortNumber;
    private String dbUserId;
    private String dbUserPassword;
    private String wsServiceName;
    private String wsHostName;
    private String wsPortNumber;
    private String wsSchema;
    private String wsUserId;
    private String wsUserPassword;

    // Getters and setters for each field
    public String getEnvName() {
        return envName;
    }

    public void setEnvName(String envName) {
        this.envName = envName;
    }

    public String getWebsiteUrl() {
        return websiteUrl;
    }

    public void setWebsiteUrl(String websiteUrl) {
        this.websiteUrl = websiteUrl;
    }

    public String getDbHostName() {
        return dbHostName;
    }

    public void setDbHostName(String dbHostName) {
        this.dbHostName = dbHostName;
    }

    public String getDbName() {
        return dbName;
    }

    public void setDbName(String dbName) {
        this.dbName = dbName;
    }

    public String getDbPortNumber() {
        return dbPortNumber;
    }

    public void setDbPortNumber(String dbPortNumber) {
        this.dbPortNumber = dbPortNumber;
    }

    public String getDbUserId() {
        return dbUserId;
    }

    public void setDbUserId(String dbUserId) {
        this.dbUserId = dbUserId;
    }

    public String getDbUserPassword() {
        return dbUserPassword;
    }

    public void setDbUserPassword(String dbUserPassword) {
        this.dbUserPassword = dbUserPassword;
    }

    public String getWsServiceName() {
        return wsServiceName;
    }

    public void setWsServiceName(String wsServiceName) {
        this.wsServiceName = wsServiceName;
    }

    public String getWsHostName() {
        return wsHostName;
    }

    public void setWsHostName(String wsHostName) {
        this.wsHostName = wsHostName;
    }

    public String getWsPortNumber() {
        return wsPortNumber;
    }

    public void setWsPortNumber(String wsPortNumber) {
        this.wsPortNumber = wsPortNumber;
    }

    public String getWsSchema() {
        return wsSchema;
    }

    public void setWsSchema(String wsSchema) {
        this.wsSchema = wsSchema;
    }

    public String getWsUserId() {
        return wsUserId;
    }

    public void setWsUserId(String wsUserId) {
        this.wsUserId = wsUserId;
    }

    public String getWsUserPassword() {
        return wsUserPassword;
    }

    public void setWsUserPassword(String wsUserPassword) {
        this.wsUserPassword = wsUserPassword;
    }
}